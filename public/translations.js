// Current language state
let currentLanguage = 'en';

// Function to toggle language
function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'fr' : 'en';
    updateLanguage();
}

// Function to update language toggle buttons
function updateLanguageButtons() {
    const currentLangElement = document.getElementById('current-lang');
    const mobileLangElement = document.getElementById('mobile-current-lang');
    
    if (currentLangElement) {
        currentLangElement.textContent = currentLanguage.toUpperCase();
    }
    if (mobileLangElement) {
        mobileLangElement.textContent = currentLanguage.toUpperCase();
    }
}

// Function to update navigation text
function updateNavigation() {
    const t = translations[currentLanguage];
    
    // Update desktop navigation
    const homeLink = document.querySelector('a[href="homepage.html"]:not(.block)');
    const aiSolutionsLink = document.querySelector('a[href="ai_solutions_services.html"]:not(.block)');
    const aboutLink = document.querySelector('a[href="about_infera_ai.html"]:not(.block)');
    const contactLink = document.querySelector('a[href="contact_executive_consultation.html"]:not(.block)');
    
    if (homeLink) homeLink.textContent = t.home;
    if (aiSolutionsLink) aiSolutionsLink.textContent = t.aiSolutions;
    if (aboutLink) aboutLink.textContent = t.about;
    if (contactLink) {
        contactLink.textContent = contactLink.textContent.includes('Schedule') ? t.scheduleConsultation : t.getFreeAssessment;
    }
    
    // Update mobile navigation
    const mobileHomeLink = document.querySelector('.md\\:hidden a[href="homepage.html"]');
    const mobileAiSolutionsLink = document.querySelector('.md\\:hidden a[href="ai_solutions_services.html"]');
    const mobileAboutLink = document.querySelector('.md\\:hidden a[href="about_infera_ai.html"]');
    const mobileContactLink = document.querySelector('.md\\:hidden a[href="contact_executive_consultation.html"]');
    
    if (mobileHomeLink) mobileHomeLink.textContent = t.home;
    if (mobileAiSolutionsLink) mobileAiSolutionsLink.textContent = t.aiSolutions;
    if (mobileAboutLink) mobileAboutLink.textContent = t.about;
    if (mobileContactLink) {
        mobileContactLink.textContent = mobileContactLink.textContent.includes('Schedule') ? t.scheduleConsultation : t.getFreeAssessment;
    }
}

// Function to update common elements
function updateCommonElements() {
    const t = translations[currentLanguage];
    
    // Update footer
    const footerDesc = document.querySelector('footer p.text-primary-200');
    if (footerDesc) footerDesc.textContent = t.footerDesc;
    
    const operatingRegions = document.querySelector('h4:contains("Current Regions")');
    if (operatingRegions) operatingRegions.textContent = t.operatingRegions;
    
    const getStartedTitle = document.querySelector('h4:contains("Get Started")');
    if (getStartedTitle) getStartedTitle.textContent = t.getStarted;
    
    const copyright = document.querySelector('footer p:last-child');
    if (copyright) copyright.textContent = t.copyright;
}

// Function to update page-specific content
function updatePageContent() {
    const t = translations[currentLanguage];
    const currentPage = window.location.pathname.split('/').pop();
    
    switch(currentPage) {
        case 'homepage.html':
            updateHomepageContent(t);
            break;
        case 'about_infera_ai.html':
            updateAboutContent(t);
            break;
        case 'ai_solutions_services.html':
            updateSolutionsContent(t);
            break;
        case 'contact_executive_consultation.html':
            updateContactContent(t);
            break;
    }
}

// Homepage specific updates
function updateHomepageContent(t) {
    // Update hero section
    const heroTitle = document.querySelector('h1');
    if (heroTitle) heroTitle.innerHTML = t.heroTitle.replace('AI Automation', '<span class="text-accent">AI Automation</span>');
    
    const heroSubtitle = document.querySelector('h1 + p');
    if (heroSubtitle) heroSubtitle.textContent = t.heroSubtitle;
    
    // Update ROI calculator
    const roiTitle = document.querySelector('h3');
    if (roiTitle) roiTitle.textContent = t.roiTitle;
    
    // Update solutions section
    const solutionsTitle = document.querySelector('.text-3xl.lg\\:text-4xl.font-bold.text-primary');
    if (solutionsTitle) solutionsTitle.textContent = t.solutionsTitle;
}

// About page specific updates
function updateAboutContent(t) {
    const heroTitle = document.querySelector('h1');
    if (heroTitle) heroTitle.innerHTML = t.aboutHeroTitle.replace('Business Automation', '<span class="text-accent">Business Automation</span>');
    
    const heroSubtitle = document.querySelector('h1 + p');
    if (heroSubtitle) heroSubtitle.textContent = t.aboutHeroSubtitle;
}

// Solutions page specific updates
function updateSolutionsContent(t) {
    const heroTitle = document.querySelector('h1');
    if (heroTitle) heroTitle.innerHTML = t.solutionsHeroTitle.replace('AI Automation', '<span class="text-accent">AI Automation</span>');
    
    const heroSubtitle = document.querySelector('h1 + p');
    if (heroSubtitle) heroSubtitle.textContent = t.solutionsHeroSubtitle;
}

// Contact page specific updates
function updateContactContent(t) {
    const heroTitle = document.querySelector('h1');
    if (heroTitle) heroTitle.innerHTML = t.contactHeroTitle.replace('Automation', '<span class="text-accent">Automation</span>');
    
    const heroSubtitle = document.querySelector('h1 + p');
    if (heroSubtitle) heroSubtitle.textContent = t.contactHeroSubtitle;
}

// Main function to update all language elements
function updateLanguage() {
    updateLanguageButtons();
    updateNavigation();
    updateCommonElements();
    updatePageContent();
    
    // Save language preference
    localStorage.setItem('inferaAiLanguage', currentLanguage);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    const savedLanguage = localStorage.getItem('inferaAiLanguage');
    if (savedLanguage && savedLanguage !== 'en') {
        currentLanguage = savedLanguage;
        updateLanguage();
    }
});

// Mobile menu toggle function
function toggleMobileMenu() {
    const menu = document.getElementById('mobile-menu');
    if (menu) {
        menu.classList.toggle('hidden');
    }
}
